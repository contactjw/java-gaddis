package chapter6;

public class Pet {
	private String name;
	private String animal;
	private int age;
	
	public void setName(String n) {
		name = n;
	}
	
	public void setAnimal(String a) {
		animal = a;
	}
	
	public void setAge(int a) {
		age = a;
	}
	
	public String getName() {
		return name;
	}
	
	public String getAnimal() {
		return animal;
	}
	
	public int getAge() {
		return age;
	}
  
}
